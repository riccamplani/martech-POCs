"""Produce the human-reviewable cleanup plan and a starter dbt staging model."""
from __future__ import annotations

import csv
from pathlib import Path

from .describe import FieldMeta

OUTPUT = Path("output")


def write_plan(sobject: str, fields: list[FieldMeta], recs: list[dict], refs: dict) -> None:
    OUTPUT.mkdir(exist_ok=True)
    rec_by_name = {r["name"]: r for r in recs}

    # CSV (machine-readable / for spreadsheets)
    csv_path = OUTPUT / f"{sobject.lower()}_cleanup_plan.csv"
    with csv_path.open("w", newline="") as fh:
        w = csv.writer(fh)
        w.writerow(["field", "type", "custom", "population_rate",
                    "referenced_count", "recommendation", "reason"])
        for f in fields:
            r = rec_by_name.get(f.name, {})
            w.writerow([f.name, f.type, f.custom, f.population_rate,
                        f.referenced_count,
                        r.get("recommendation", "REVIEW"),
                        r.get("reason", "")])

    keep = [f.name for f in fields if rec_by_name.get(f.name, {}).get("recommendation") == "KEEP"]
    deprecate = [r for r in recs if r["recommendation"] == "DEPRECATE"]
    review = [r for r in recs if r["recommendation"] == "REVIEW"]

    # Markdown summary (human review)
    md = [f"# {sobject} field cleanup plan", "",
          f"- Total fields analyzed: **{len(fields)}**",
          f"- KEEP: **{len(keep)}**",
          f"- DEPRECATE: **{len(deprecate)}**",
          f"- REVIEW: **{len(review)}**", "",
          "## Proposed to deprecate", ""]
    for r in deprecate:
        md.append(f"- `{r['name']}` — {r['reason']}")
    md += ["", "## Needs human review", ""]
    for r in review:
        md.append(f"- `{r['name']}` — {r['reason']}")
    (OUTPUT / f"{sobject.lower()}_cleanup_plan.md").write_text("\n".join(md))

    _write_dbt_model(sobject, keep)


def _write_dbt_model(sobject: str, keep_fields: list[str]) -> None:
    """The KEEP list becomes the column projection for the dbt staging model.
    This is the bridge into the CRMA -> BigQuery/dbt migration (phase 2)."""
    cols = ",\n        ".join(keep_fields) if keep_fields else "id"
    sql = f"""-- generated by sfcleanup: only the fields that survived cleanup
with source as (
    select * from {{{{ source('salesforce', '{sobject.lower()}') }}}}
)
select
        {cols}
from source
"""
    dest = Path("dbt/models/staging")
    dest.mkdir(parents=True, exist_ok=True)
    (dest / f"stg_salesforce__{sobject.lower()}s.sql").write_text(sql)
